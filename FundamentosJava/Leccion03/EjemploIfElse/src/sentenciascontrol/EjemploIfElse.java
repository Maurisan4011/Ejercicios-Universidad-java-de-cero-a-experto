package sentenciascontrol;

public class EjemploIfElse {

    public static void main(String[] args) {
        // Ejemplo if/else
        int x = 30;
        if (x < 20) {
            System.out.println("X menos que 20\n");
        } else {
            System.out.println("X mayor que 20\n");
        }
    }
}
